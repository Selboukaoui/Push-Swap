#include "push_swap.h"

void	err(void)
{
	write(2, "Error\n", 6);
	exit(1);
}

int	main(int ac, char **av)
{
	t_stack	*stack_a;
	t_stack	*stack_b;

	stack_a = NULL;
	stack_b = NULL;
	if (ac == 1)
		return (0);
	av = check_av(ac, av);
	if (is_sorted(av) == 1)
		return (0);
	stack_a = add_args_to_a(av, &stack_a);
	get_index(&stack_a);
	sorting(&stack_a, &stack_b);
	return (0);
}
